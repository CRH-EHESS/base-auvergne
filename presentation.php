<li class="contenttimes"><strong><a href="/auvergne/sommaire.php" target="_blank">Le Corpus topo-photographique des chapiteaux</a></strong> permet de s'interroger sur l'agencement des chapiteaux en les localisant et en les visualisant à leurs emplacements respectifs pour mieux saisir leurs relations. Jusqu'à présent, il n'existait pas d'outil répondant de manière satisfaisante à une telle exigence. Il nous a donc fallu l'élaborer, ce que nous avons fait en adaptant et en intégrant les logiciels libres <a href="http://tapor.uvic.ca/~mholmes/image_markup/" target="_blank"><span class="liengris">Image Markup Tool</span></a> et <a href="http://albulle.jebulle.net/" target="_blank"><span class="liengris">Albulle</span></a>, avec l'aide de Marjorie Burghart, de Rubi Cortes (de la société des Mots & des Mains) et de Martin Holmes (IMT). La mise en œuvre du site a été réalisée par Chloé Maillet.<br />
 Ce corpus est accessible à tous : libre à chacun de se l'approprier à sa manière, pour circuler dans l'édifice ou pour produire du matériel pédagogique, en ayant ou non recours à la méthode d'analyse cartographique que nous proposons dans le second volet du site. Les clichés du GAHOM sont téléchargeables et utilisables à des fins non commerciales, conformément à la licence <a href="http://creativecommons.org/licenses/by-nc/3.0/" target="_blank"><span class="liengris">Creative Commons (BY-NC)</span></a>. Cet outil de visualisation des images est également disponible gratuitement pour traiter d'autres édifices, voire d'autres types d'images (nous contacter : <a href="mailto:dittmar@ehess.fr"><span class="liengris">dittmar@ehess.fr</span></a>). <br />
